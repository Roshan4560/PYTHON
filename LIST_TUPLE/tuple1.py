# TUPLE  METHODS 

tup = (1, 2, 3, 4, 5, 6, 7, 2, 2, 10)

# 1. tup.count() - returns the number of times a specified value occurs in a tuple.
print(tup.count(2)) # 3  total number of 2 in the tuple

# 2. tup.index() - returns the index of the first occurrence of a specified value.
print(tup.index(2)) # 1  # first occurrence of 2 in the tuple