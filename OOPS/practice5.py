#  IMPORTANT POINTS 

# 1. Absraction : hiding the implementation details of a class and only showing the essential features to the user.
#  means ye unnecessary chejee nahi dikhata hai


# 2. Encapsulation : wrapping the data and the functions into a single unit(object).
#  means ye data ko function ke andar wrap kar deta hai

# 3. Inheritance : inheriting the properties of a class into another class.
#  means ye ek class ki properties dusri class ko de deta hai

# 4. Polymorphism : the ability to take many forms.
#  means ye ek function ko alag alag tarike se use karne deta hai